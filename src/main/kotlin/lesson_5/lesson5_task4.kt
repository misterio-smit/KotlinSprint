package org.example.lesson_5

fun main() {
    val registeredUser = "Zaphod"
    val correctPassword = "PanGalactic"

    // Вступление Марвина
    println(
        """
        Внимание, пассажир. Моя обязанность, как обычно, незавидная, - приветствовать вас на борту корабля "Heart of Gold". 
        [вздыхает...] Что ж, введи, пожалуйста, свое имя пользователя, чтобы мы могли приступить к процессу входа. 
        Я всегда готов служить, хотя это не приносит мне радости... но вы, наверное, об этом не заботитесь. 
        [вздыхает ещё глубже...] Да, вперед, пожалуйста, вводите свои данные...
    """.trimIndent()
    )

    print("\nВведите имя пользователя: ")
    val username = readLine()

    if (username != registeredUser) {
        println(
            """
            [глубокий вздох...] Похоже, вы не зарегистрированы в системе. 
            Я мог бы помочь вам с регистрацией, но честно говоря, мне это совершенно неинтересно...
            [звук отключения системы...]
            Процесс завершен. До свидания. Если вам это вообще нужно...
        """.trimIndent()
        )
        return
    }

    println("\n[монотонно] О, это вы, $username... Ну что ж, введите пароль, если вам так уж необходимо...")
    print("Введите пароль: ")
    val password = readLine()

    if (password != correctPassword) {
        println(
            """
            [разочарованно] Неверный пароль. Как и следовало ожидать. 
            Впрочем, я даже не знаю, зачем вы пытаетесь... 
            [система блокируется...]
            Доступ запрещен. Хотя вам, наверное, все равно...
        """.trimIndent()
        )
        return
    }

    println(
        """
        [вздыхает...] Ваши данные проверены, и о, чудо, они верны... 
        Пользователь "$username", вам разрешено входить на борт корабля "Heart of Gold". 
        Хотя мне всё равно... Ну вперед, войдите... Если вам так уж надо, в конце концов... 
        [меланхолический вздох...] Надеюсь, вам понравится пребывание здесь больше, чем мне.
    """.trimIndent()
    )

    // Дополнительные действия для авторизованного пользователя
    println("\n[бортовой компьютер активирован]")
    println("1. Включить двигатели бесконечной невероятности")
    println("2. Настроить навигацию по Вселенной")
    println("3. Включить чайник")
    print("Выберите действие: ")

    when (readLine()) {
        "1" -> println("\n[глухой гул] Двигатели активированы. Хотя какая разница...")
        "2" -> println("\n[скучно] Навигация включена. Мы летим... куда-то...")
        "3" -> println("\n[разочарованно] Чайник включен. Какой в этом смысл...")
        else -> println("\n[вздох] Вы даже выбрать не можете... Ладно...")
    }
}